// Get translation function
export function getTranslation(locale = DEFAULT_LOCALE) {
  return translations[locale] || translations[DEFAULT_LOCALE];
}

// Get browser locale
export function getBrowserLocale() {
  if (typeof window === 'undefined') return DEFAULT_LOCALE;
  const browserLang = navigator.language?.split('-')[0];
  return SUPPORTED_LOCALES.includes(browserLang) ? browserLang : DEFAULT_LOCALE;
}

// Storage key
const LOCALE_STORAGE_KEY = 'leapifyes_locale';

// Get saved locale
export function getSavedLocale() {
  if (typeof window === 'undefined') return DEFAULT_LOCALE;
  return localStorage.getItem(LOCALE_STORAGE_KEY) || getBrowserLocale();
}

// Save locale
export function saveLocale(locale) {
  if (typeof window !== 'undefined' && SUPPORTED_LOCALES.includes(locale)) {
    localStorage.setItem(LOCALE_STORAGE_KEY, locale);
  }
}
